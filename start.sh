#!/bin/bash
#===============================================================================
# WordPress + MariaDB 备份管理 - 主入口脚本
# 文件：start.sh
# 用途：交互式菜单，执行环境检测和备份操作
#===============================================================================

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# 加载配置文件
if [ -f "${SCRIPT_DIR}/data.conf" ]; then
    source "${SCRIPT_DIR}/data.conf"
else
    echo -e "\033[31m[ERROR] 配置文件 data.conf 不存在\033[0m"
    exit 1
fi

#===============================================================================
# 颜色定义
#===============================================================================
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

#===============================================================================
# 显示主菜单
#===============================================================================
show_menu() {
    clear
    echo -e "${CYAN}========================================${NC}"
    echo -e "${CYAN}  WordPress + MariaDB 备份管理脚本${NC}"
    echo -e "${CYAN}========================================${NC}"
    echo ""
    echo -e "${GREEN}请选择操作：${NC}"
    echo ""
    echo -e "  ${GREEN}1.${NC} 环境检测   - 检查系统环境是否满足备份要求"
    echo -e "  ${GREEN}2.${NC} 执行备份   - 备份数据库和 WordPress 文件"
    echo -e "  ${GREEN}3.${NC} 帮助信息   - 显示使用说明"
    echo -e "  ${GREEN}0.${NC} 退出       - 退出脚本"
    echo ""
    echo -e "${CYAN}========================================${NC}"
    echo -n "请输入选项 [0-3]: "
}

#===============================================================================
# 执行环境检测
#===============================================================================
run_check() {
    echo ""
    echo -e "${YELLOW}正在执行环境检测...${NC}"
    echo ""

    if [ -f "${SCRIPT_DIR}/modules/path.sh" ]; then
        chmod +x "${SCRIPT_DIR}/modules/path.sh"
        "${SCRIPT_DIR}/modules/path.sh"
        return $?
    else
        echo -e "${RED}[ERROR] 环境检测模块不存在：modules/path.sh${NC}"
        return 1
    fi
}

#===============================================================================
# 执行备份
#===============================================================================
run_backup() {
    echo ""
    echo -e "${YELLOW}正在执行备份...${NC}"
    echo ""

    if [ -f "${SCRIPT_DIR}/modules/backup.sh" ]; then
        chmod +x "${SCRIPT_DIR}/modules/backup.sh"
        "${SCRIPT_DIR}/modules/backup.sh"
        return $?
    else
        echo -e "${RED}[ERROR] 备份模块不存在：modules/backup.sh${NC}"
        return 1
    fi
}

#===============================================================================
# 显示帮助信息
#===============================================================================
show_help() {
    cat << EOF

========================================
  帮助信息
========================================

【使用说明】

本脚本用于自动备份 WordPress 网站和 MariaDB 数据库。

【菜单选项】

  1. 环境检测
     检查系统环境是否满足备份要求，包括：
     - 必要命令 (tar, gzip, mysqldump 等)
     - PHP 环境
     - MariaDB 数据库连接
     - WordPress 目录
     - 目录权限
     - Web 服务状态

  2. 执行备份
     执行完整的备份操作，包括：
     - 备份 MariaDB 数据库
     - 备份 WordPress 文件
     - 自动清理旧备份
     - 验证备份完整性

  3. 帮助信息
     显示本帮助信息

  0. 退出
     退出脚本

【配置文件】

配置文件: ${SCRIPT_DIR}/data.conf

请确保在使用前配置好 data.conf 文件中的数据库信息。

【定时任务】

可以设置 crontab 定时自动执行备份：

  crontab -e

添加以下行（每天凌晨 3 点执行）：

  0 3 * * * ${SCRIPT_DIR}/start.sh backup

EOF
}

#===============================================================================
# 暂停等待用户按回车
#===============================================================================
pause() {
    echo ""
    echo -n "按回车键继续..."
    read -r
}

#===============================================================================
# 主程序
#===============================================================================
main() {
    # 检查是否为非交互模式（通过命令行参数）
    if [ -n "$1" ]; then
        case "$1" in
            check)
                run_check
                exit $?
                ;;
            backup)
                run_backup
                exit $?
                ;;
            help|--help|-h)
                show_help
                exit 0
                ;;
            *)
                echo -e "${RED}[ERROR] 未知参数：${1}${NC}"
                echo "使用方法: $0 或 $0 {check|backup|help}"
                exit 1
                ;;
        esac
    fi

    # 交互式菜单模式
    while true; do
        show_menu
        read -r choice
        echo ""

        case "$choice" in
            1)
                run_check
                pause
                ;;
            2)
                run_backup
                pause
                ;;
            3)
                show_help
                pause
                ;;
            0)
                echo -e "${GREEN}再见！${NC}"
                exit 0
                ;;
            *)
                echo -e "${RED}无效选项，请重新选择${NC}"
                pause
                ;;
        esac
    done
}

# 执行主程序
main "$@"
